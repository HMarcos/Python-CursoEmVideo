n = input('Digite algo: ')
print(n.isnumeric())  # Se pode ser convertido em um número
print(n.isalpha())  # Se são apenas letras
print(n.isalnum())  # Se são letras ou números
print(n.isupper())  # Se todas os caracteres são maiúsculos
print(n.islower())  # Se todas os caracteres são minúsculos
